# ## Задача №2
# Напишете програма, която генерира при подадена целочислена положителна стойност следния pattern:
# ```
# 5
# ```
# ```
# 1 2 3 4 5
# 1 4 9 16 25
# 1 8 27 64 125
# 1 16 81 256 625
# 1 32 243 1024 3125
# ```

n = 5

for i in range(1, n+1):
    for k in range(1, n+1):
        print(k**i, end=" ")
    print()
