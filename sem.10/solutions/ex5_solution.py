"""
Напишете рекурсивна функция, която приема неотрицателно цяло число n в интервала (0,1000), превръща го в двоично число, записва го в променлива и го връща като резултат.
Проверете входа за валидност.
Вход: 123
Изход: 1111011
"""

def decimal_to_binary(n):
    if n == 1:
        return n

    return n % 2 + 10*decimal_to_binary(n//2)


print(decimal_to_binary(123))
